// Add perfume_detail to variants
mockVariants.forEach(variant => {
  const perfume = mockPerfumes.find(p => p.id === variant.perfume);
  if (perfume) {
    variant.perfume_detail = { ...perfume };
    variant.perfume_detail.category_detail = mockCategories.find(c => c.id === perfume.category);
  }
});

// Add category_detail to perfumes
mockPerfumes.forEach(perfume => {
  perfume.category_detail = mockCategories.find(c => c.id === perfume.category);
  perfume.variants = mockVariants.filter(v => v.perfume === perfume.id);
});

